package com.study;

public class Demo10 {

    /**
     * Which option can replace N1 to enable the code to print 135?
     * @param args
     */
    public static void main(String[] args) {
        int a[] ={1,2,3,4,5};
        for (int e = 0; e< 5 ; e+=2){ //N1
            System.out.print(a[e]);
        }
    }
}
